Ce site groupe toutes les informations nécessaire pour suivre l'actualité et la compétition sur la franchise Battlefield,

Dans la partie actualité nous sélectionnons 3 articles qui nous semble pertinent pour les joueurs, les membres peuvent nous proposé des articles qu'ils trouvent intéressent.

Les chaînes twitch en lives sur le jeu sont affiché ainsi que les dernières vidéos youtube de la communauté, ce qu'ils leur permettra d'avoir une visibilité supplémentaire et de découvrir de nouvelle personnes.

Une partie membre affiche tous les membres inscrit sur le site, un click redirige vers leur profil.

La page profil joue un rôle important, elle permet au joueurs qui n'ont souvent pas la visibilité qu'il mérite de ce faire connaître, ils peuvent afficher leur states en jeu ainsi que leur réseaux sociaux.
Les states sont récupéré depuis le site battlefieldtracker.com, cela permet d’accéder à leur profil rapidement sans devoir chercher leur identifiant origin.

la sections Compétitions :

les compétions eSport sur le jeu vidéo Battlefield sont souvent difficile à suivre, les matchs et événements qui sont organisé sont la plupart du temps répartie sur différente plate-forme, les matchs s'organise souvent à la dernière minutes suivant les disponibilités des joueurs des 2 équipes,

Ce site regroupe toutes les informations dont les joueurs et supporters ont besoin pour suivre les matchs et tournois de leur équipe ou équipe favorite, Cela permet d'avoir un coté plus visuel sur l'activité eSport de Battlefield

	les équipes
Les équipes les plus active seront répertorier avec leur joueurs les plus actifs
Cela peut permettre aux équipes et joueurs de ce faire connaître 
Si les joueurs sont inscrit sur le site on auras la possibilité d'aller voir leur profils

	les tournois
Les derniers tournois seront répertorier avec les équipes qui y participe
Un click redirige directement vers la page du tournoi officiel

	Les matchs récents
Les derniers matchs seront répertorier avec toutes les informations indispensable
les 2 équipes, le score, les cartes, le tournoi, la date, la VOD
Un click redirige vers la vidéo pour revoir le match

	Les casters 
Les casters actifs seront répertorier
Un click redirige vers leur chaine twitch 


